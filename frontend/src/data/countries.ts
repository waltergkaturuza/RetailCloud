/**
 * Get country by name
 */
export function getCountryByName(name: string): Country | undefined {
  return COUNTRIES.find(c => c.name.toLowerCase() === name.toLowerCase())
}

/**
 * Get country by code
 */
export function getCountryByCode(code: string): Country | undefined {
  return COUNTRIES.find(c => c.code === code || c.iso2 === code || c.iso3 === code)
}

/**
 * Search countries by name (case-insensitive)
 */
export function searchCountries(query: string): Country[] {
  const lowerQuery = query.toLowerCase()
  return COUNTRIES.filter(c => 
    c.name.toLowerCase().includes(lowerQuery) ||
    c.code.toLowerCase().includes(lowerQuery) ||
    c.iso2.toLowerCase().includes(lowerQuery) ||
    c.iso3.toLowerCase().includes(lowerQuery)
  )
}

/**
 * Get country code from country name (helper for state/province integration)
 */
export function getCountryCodeFromName(countryName: string): string | undefined {
  const country = getCountryByName(countryName)
  return country?.code
}

